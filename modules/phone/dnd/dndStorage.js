// modules/phone/dnd/dndStorage.js — D&D Data Persistence + Cloud Sync
// localStorage (primary) + server sync (backup/cross-device).
// Follows the same pattern as treeStorage.js.

import { getContext } from '../../../../../../extensions.js';
import { apiRequest } from '../moments/apiClient.js';
import { getSettings } from '../moments/state.js';
import { getCampaignById } from './dndCampaigns.js';

const DND_STORAGE_KEY = 'gf_dnd_data_v1';
const DND_LOG = '[D&D]';

// ═══════════════════════════════════════════════════════════════════════
// Default Data Structure
// ═══════════════════════════════════════════════════════════════════════

function createDefaultData() {
    return {
        playerCharacter: null,      // Created via character creation flow
        partnerCharacter: null,     // Auto-generated by LLM

        currentRun: null,           // Active adventure (see below for shape)
        // currentRun shape:
        // {
        //     campaignId: 'lost_mine',
        //     currentRoom: 1,
        //     totalRooms: 4,
        //     narrativeLog: [{ type:'dm'|'action'|'dice'|'partner', text:'...' }],
        //     roomType: 'combat',         // current room event type
        //     inCombat: false,
        //     combatState: null,          // { enemies, turnOrder, currentTurn }
        //     startedAt: '',
        // }

        history: [],
        // history item shape:
        // {
        //     date: '2026-03-16',
        //     campaign: '失落的矿坑',
        //     campaignId: 'lost_mine',
        //     result: 'victory' | 'defeat' | 'retreat',
        //     highlights: 'Nat 20 一箭射中了...',
        //     xpGained: 300,
        //     loot: ['治疗药水', '+1 长剑'],
        //     roomsCleared: 4,
        // }

        _version: 1,
        _updatedAt: '',
    };
}

// ═══════════════════════════════════════════════════════════════════════
// Core Read/Write
// ═══════════════════════════════════════════════════════════════════════

/**
 * Load D&D data from localStorage.
 */
export function loadDndData() {
    try {
        const raw = localStorage.getItem(DND_STORAGE_KEY);
        if (!raw) return createDefaultData();
        const parsed = JSON.parse(raw);
        return deepMerge(createDefaultData(), parsed);
    } catch (e) {
        console.warn(`${DND_LOG} loadDndData failed:`, e);
        return createDefaultData();
    }
}

/**
 * Save D&D data to localStorage + async sync to server.
 */
export function saveDndData(data) {
    data._updatedAt = new Date().toISOString();
    try {
        localStorage.setItem(DND_STORAGE_KEY, JSON.stringify(data));
    } catch (e) {
        console.error(`${DND_LOG} saveDndData failed:`, e);
    }
    _syncToServer(data);
}

