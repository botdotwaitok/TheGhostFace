// ═══════════════════════════════════════════════════════════════════════
// Recently-used tracking (localStorage)
// ═══════════════════════════════════════════════════════════════════════

const RECENT_KEY = 'gf_chat_recent_emojis';
const RECENT_MAX = 24;

export function getRecentEmojis() {
    try {
        const raw = localStorage.getItem(RECENT_KEY);
        if (!raw) return [];
        const arr = JSON.parse(raw);
        return Array.isArray(arr) ? arr.slice(0, RECENT_MAX) : [];
    } catch {
        return [];
    }
}

/**
 * Move (or add) an emoji to the front of the recent list. Deduplicates.
 */
export function pushRecentEmoji(emoji) {
    if (!emoji || typeof emoji !== 'string') return;
    try {
        const cur = getRecentEmojis();
        const next = [emoji, ...cur.filter(e => e !== emoji)].slice(0, RECENT_MAX);
        localStorage.setItem(RECENT_KEY, JSON.stringify(next));
    } catch {
        // localStorage full or disabled — recents are non-essential, swallow
    }
}
